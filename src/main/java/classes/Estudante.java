package classes;


public class Estudante {
	
	private int codigo;
	private String nome;
	private String anoCursoAtual;
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getAnoCursoAtual() {
		return anoCursoAtual;
	}
	public void setAnoCursoAtual(String anoCursoAtual) {
		this.anoCursoAtual = anoCursoAtual;
	}
	
}
